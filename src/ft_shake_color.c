#include "fractol.h"

int		ft_shake_color(t_fr *fr)
{
	fr->cl.g = ((fr->color >> 8) & 0xFF);
	fr->cl.b = (fr->color & 0xFF);
	fr->cl.r = fr->i * 20;
	return ((fr->cl.r << 16) + (fr->cl.g << 8) + fr->cl.b);
}
